public class MixedTypeAddition { public static void main(String[] args) {
    // Declare and assign values to an integer and a double variable
    int num1 = 10;
    double num2 = 4.5;

    // Add the integer and double variables
    double sum = num1 + num2;  // Result will automatically be promoted to double

    // Print out the result
    System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
}

}

